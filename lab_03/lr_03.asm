STKS SEGMENT PARA STACK 'STACK'
    DB 100 DUP (?)
STKS ENDS

DATAS SEGMENT PARA 'DATA'
    ROW DB 8
DATAS ENDS

CODES SEGMENT PARA 'CODE'
    ASSUME CS:CODES, DS:DATAS
    
UPPER:
    MOV DL, ROW + 5
    SUB DL, 32
    RET
    
OUTPUT:
    MOV AH, 2
    MOV DL, 13
    INT 21H
    MOV DL, 10
    INT 21H
    
    CALL UPPER
    
    INT 21H
    RET
    
MAIN:
    MOV AX, DATAS
    MOV DS, AX
    MOV DX, OFFSET ROW
    MOV AH, 0AH
    INT 21H

    CALL OUTPUT
    
    MOV AH,4Ch
    INT 21H
CODES ENDS
END MAIN
